package org.shishir.designprinciple.openclose;
/**
 * The Open/Closed Principle (OCP) states that:
 * "A class should be open for extension but closed for modification."
 *
 * This means that a class should allow adding new functionality without
 * modifying existing code. Instead of altering existing classes, we should
 * extend them using inheritance, polymorphism, or interfaces.
 */