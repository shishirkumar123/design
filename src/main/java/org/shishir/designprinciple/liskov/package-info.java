package org.shishir.designprinciple.liskov;

/**
 * The Liskov Substitution Principle (LSP) states that a subclass should be
 * able to replace its superclass without altering the correctness of the program.
 * This means that if class B is a subclass of class A, then A's objects should
 * be replaceable by B's objects without unexpected behavior.
 *
 * Violation of LSP happens when a subclass changes the expected behavior
 * of its superclass.
 */